
public class originJava02 {
public static void main(String[] args) {
	
	// println은 자동으로 줄바꿈을 실행함
	System.out.println("Hello world!");
	System.out.println("Hello world!");
	System.out.println("Hello world!");
	
	// print는 자동으로 줄바꿈을 실행하지않음. 
	System.out.print("Hello World");
	System.out.print("Hello World");
	System.out.print("Hello World");
	System.out.println("----------");
	
	
	System.out.println("3+5"); // " " 안에 나온것은 숫자 그 자체로 표시되는게 아닌 문자로 표시됨.
	
	System.out.println(3+5);  //Plus
	System.out.println(5-3);  //Minus
	System.out.println(3*5);  //Times
	System.out.println(5/3); //Divide
	
	
	
	
	
	
	
	
	
	
	
	
 }
}
