let total = 12000 + 25000 + 13000;  // total 이란 이름의 변수를 선언하고 옆에 있는 값들을 total에 대입해서 
console.log("total = " + total); 

// 변수를 만들고 선언하는 방법은 let , const , var 총 3가지가 있다. 

/*
1. 먼저 let으로 시작하는 명령을 선언문 이라고 한다. total은 변수명이다
2. 변수를 선언함과 동시에 값을 대입하는 행위를 초기화라고 한다.
*/

